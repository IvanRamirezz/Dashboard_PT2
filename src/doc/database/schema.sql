CREATE TYPE estado_aprobacion AS ENUM (
  'pendiente',
  'aprobado',
  'rechazado'
);


-- =====================================================
-- TABLAS
-- =====================================================

-- Tabla base de usuarios (alumnos, profesores y admins)
CREATE TABLE usuarios (
  usuario_id        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre            VARCHAR NOT NULL,
  apellido_paterno  VARCHAR NOT NULL,
  apellido_materno  VARCHAR NOT NULL,
  auth_uid          UUID NOT NULL UNIQUE REFERENCES auth.users(id) ON DELETE CASCADE,
  active_session_uuid UUID,   -- control de sesión única por dispositivo
  created_at        TIMESTAMP DEFAULT now()
);

CREATE TABLE profesores (
  profesor_id         INT PRIMARY KEY REFERENCES usuarios(usuario_id) ON DELETE CASCADE,
  matricula_trabajador VARCHAR NOT NULL UNIQUE,
  estado              estado_aprobacion DEFAULT 'pendiente'
);

CREATE TABLE administrador (
  admin_id INT PRIMARY KEY REFERENCES usuarios(usuario_id) ON DELETE CASCADE
);

CREATE TABLE grupos (
  grupo_id      INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre        VARCHAR NOT NULL,
  codigo_acceso VARCHAR NOT NULL UNIQUE,
  ciclo_escolar VARCHAR NOT NULL,
  profesor_id   INT REFERENCES profesores(profesor_id),
  activo        BOOLEAN DEFAULT true
);

CREATE TABLE alumnos (
  alumno_id INT PRIMARY KEY REFERENCES usuarios(usuario_id) ON DELETE CASCADE,
  boleta    VARCHAR NOT NULL UNIQUE,
  grupo_id  INT REFERENCES grupos(grupo_id) ON DELETE SET NULL
);

CREATE TABLE practicas (
  practica_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  titulo      VARCHAR NOT NULL,
  descripcion TEXT,
  activo      BOOLEAN DEFAULT true
);

CREATE TABLE practicas_grupo (
  asignacion_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  grupo_id      INT REFERENCES grupos(grupo_id) ON DELETE CASCADE,
  practica_id   INT REFERENCES practicas(practica_id) ON DELETE CASCADE,
  fecha_inicio  TIMESTAMP NOT NULL,
  fecha_fin     TIMESTAMP NOT NULL
);

CREATE TABLE resultados (
  resultado_id    INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  alumno_id       INT NOT NULL REFERENCES alumnos(alumno_id) ON DELETE CASCADE,
  practica_id     INT NOT NULL REFERENCES practicas(practica_id) ON DELETE CASCADE,
  calificacion    NUMERIC(4,2),
  respuestas_json JSONB,
  created_at      TIMESTAMP DEFAULT now(),
  CONSTRAINT resultados_unq UNIQUE (alumno_id, practica_id)
);

-- =====================================================
-- FUNCIÓN AUXILIAR (debe crearse antes de las policies)
-- =====================================================

CREATE OR REPLACE FUNCTION current_usuario_id()
RETURNS int
LANGUAGE sql
STABLE
SECURITY DEFINER  -- bypasea RLS al consultar usuarios, evita recursión infinita
SET search_path = public
AS $$
  SELECT usuario_id
  FROM usuarios
  WHERE auth_uid = auth.uid()
$$;

-- =====================================================
-- Obtiene el UUID de un usuario en Supabase Auth a partir de su correo electrónico.
-- =====================================================

CREATE OR REPLACE FUNCTION get_auth_uid_by_email(p_email TEXT)
RETURNS UUID
LANGUAGE sql
SECURITY DEFINER
SET search_path = auth, public
AS $$
  SELECT id FROM auth.users WHERE email = p_email LIMIT 1;
$$;

