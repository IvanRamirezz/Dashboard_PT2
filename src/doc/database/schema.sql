-- =====================================================
-- POLÍTICAS RLS — USUARIOS
-- =====================================================

-- El usuario ve y modifica únicamente su propio registro
CREATE POLICY "usuarios_select_propietario"
ON usuarios FOR SELECT
USING (auth_uid = auth.uid());

CREATE POLICY "usuarios_insert_propietario"
ON usuarios FOR INSERT
WITH CHECK (auth_uid = auth.uid());

-- Permite al alumno actualizar su active_session_uuid (control de sesión)
CREATE POLICY "usuario actualiza su propia sesion"
ON usuarios FOR UPDATE
TO authenticated
USING (auth_uid = auth.uid())
WITH CHECK (auth_uid = auth.uid());

-- El profesor puede ver los datos de usuario de sus alumnos
CREATE POLICY "profesor_ver_usuarios_de_sus_alumnos"
ON usuarios FOR SELECT
USING (
  usuario_id IN (
    SELECT alumno_id FROM alumnos
    WHERE grupo_id IN (
      SELECT grupo_id FROM grupos
      WHERE profesor_id = current_usuario_id()
    )
  )
);


-- =====================================================
-- POLÍTICAS RLS — PROFESORES
-- =====================================================

CREATE POLICY "profesor_ver_su_registro"
ON profesores FOR SELECT
USING (profesor_id = current_usuario_id());


-- =====================================================
-- POLÍTICAS RLS — GRUPOS
-- =====================================================


-- Profesor crea grupos solo si está aprobado
CREATE POLICY "grupos_insert_profesor_aprobado"
ON grupos FOR INSERT
WITH CHECK (
  profesor_id = current_usuario_id()
  AND EXISTS (
    SELECT 1 FROM profesores
    WHERE profesor_id = current_usuario_id()
    AND estado = 'aprobado'
  )
);

-- Profesor actualiza sus grupos (ej. dar de baja → activo = false)
CREATE POLICY "grupos_update_profesor"
ON grupos FOR UPDATE
USING (profesor_id = current_usuario_id())
WITH CHECK (profesor_id = current_usuario_id());

-- Profesor elimina sus grupos
CREATE POLICY "grupos_delete_profesor"
ON grupos FOR DELETE
USING (profesor_id = current_usuario_id());


-- Alumno puede leer grupos activos (necesario para unirse con código de acceso)
CREATE POLICY "alumnos_leer_grupos_activos"
ON grupos FOR SELECT
TO authenticated
USING (activo = true);


-- =====================================================
-- POLÍTICAS RLS — ALUMNOS
-- =====================================================

-- Alumno ve su propio registro
CREATE POLICY "alumno_ver_su_registro"
ON alumnos FOR SELECT
USING (alumno_id = current_usuario_id());

-- Alumno actualiza su propio grupo_id (al unirse a un grupo con código)
CREATE POLICY "alumno_actualizar_su_grupo"
ON alumnos FOR UPDATE
TO authenticated
USING (alumno_id = current_usuario_id())
WITH CHECK (alumno_id = current_usuario_id());

-- Profesor ve los alumnos de sus grupos
CREATE POLICY "profesor_ver_alumnos_de_sus_grupos"
ON alumnos FOR SELECT
USING (
  grupo_id IN (
    SELECT grupo_id FROM grupos
    WHERE profesor_id = current_usuario_id()
  )
);


-- =====================================================
-- POLÍTICAS RLS — PRÁCTICAS
-- =====================================================

-- Cualquier usuario autenticado puede ver prácticas activas
CREATE POLICY "ver_practicas_activas"
ON practicas FOR SELECT
USING (activo = true);


-- =====================================================
-- POLÍTICAS RLS — PRÁCTICAS POR GRUPO
-- =====================================================

-- Alumno ve las prácticas asignadas a su grupo
CREATE POLICY "alumno_ve_practicas_de_su_grupo"
ON practicas_grupo FOR SELECT
TO authenticated
USING (
  grupo_id IN (
    SELECT grupo_id FROM alumnos
    WHERE alumno_id = current_usuario_id()
  )
);

-- Profesor inserta practicas grupo
CREATE POLICY "profesor_insert_practicas_grupo"
ON practicas_grupo
FOR INSERT
TO authenticated
WITH CHECK (
  grupo_id IN (
    SELECT grupo_id
    FROM grupos
    WHERE profesor_id = current_usuario_id()
  )
);

-- =====================================================
-- POLÍTICAS RLS — RESULTADOS
-- =====================================================

-- Alumno ve sus propios resultados
CREATE POLICY "alumno_ver_resultados"
ON resultados FOR SELECT
USING (alumno_id = current_usuario_id());

-- Alumno inserta su propio resultado al finalizar el cuestionario
CREATE POLICY "alumno_inserta_su_resultado"
ON resultados FOR INSERT
TO authenticated
WITH CHECK (alumno_id = current_usuario_id());

-- Profesor ve los resultados de los alumnos de sus grupos
CREATE POLICY "profesor_ver_resultados_grupo"
ON resultados FOR SELECT
USING (
  alumno_id IN (
    SELECT alumno_id FROM alumnos
    WHERE grupo_id IN (
      SELECT grupo_id FROM grupos
      WHERE profesor_id = current_usuario_id()
    )
  )
);

-- Profesor actualiza la calificación de los resultados de sus alumnos
CREATE POLICY "profesor_actualiza_resultados"
ON resultados FOR UPDATE
USING (
  alumno_id IN (
    SELECT alumno_id FROM alumnos
    WHERE grupo_id IN (
      SELECT grupo_id FROM grupos
      WHERE profesor_id = current_usuario_id()
    )
  )
);

